  int navigation(int navig)
  {
    int navX = 25;
    int navY = 16;
    int x = 1;
    while (true)
    {
      gotoxy(navX, navY);
      cout << "=>";
      gotoxy(20, 20);
      if (GetAsyncKeyState(VK_DOWN))
      {
        if (x < navig)
        {
          x = x + 1;
          gotoxy(navX, navY);
          cout << "  ";
          navY = navY + 1;
          gotoxy(navX, navY);
          cout << "=>";
        }
      }
      if (GetAsyncKeyState(VK_UP))
      {
        if (x > 1)
        {
          x = x - 1;
          gotoxy(navX, navY);
          cout << "  ";
          navY = navY - 1;
          gotoxy(navX, navY);
          cout << "=>";
        }
      }
      if (getch() == 13)
      {
        system("cls");
        return x;
      }
      Sleep(100);
    }
  }
  void storeScore(int &index4, int highScore[], int score)
  {
    fstream myFile;
    myFile.open("score.txt", ios::out);
    index4--;
    highScore[index4] = score;
    for (int i = 0; i < index4 + 1; i++)
    {
      myFile << highScore[i] << endl;
    }

    myFile.close();
  }
  void loadScore(int highScore[], int &index4)
  {
    fstream myFile;
    myFile.open("score.txt", ios::in);
    if (myFile.is_open())
    {
      int i = 0;
      while (!myFile.eof())
      {
        myFile >> highScore[i];
        i++;
      }
      index4 = i - 1;
      myFile.close();
    }
  }
